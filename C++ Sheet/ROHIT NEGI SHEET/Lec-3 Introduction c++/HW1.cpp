// Two numbers are given, print their product


#include<iostream>
using namespace std;

int main() 
{
    int num1, num2, product;

    
    cout << "Input any two number: ";
    cin >> num1 >> num2;

    // Multiply the two numbers by the product of the two numbers and return the result.
    product = num1 * num2;

    cout << "Product = " << product << endl;


}
